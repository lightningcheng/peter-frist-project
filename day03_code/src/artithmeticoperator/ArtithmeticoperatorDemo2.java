package artithmeticoperator;

public class ArtithmeticoperatorDemo2 {
    public static void main(String[]args){
        //除法
        System.out.println(10/2);
        System.out.println(10/3);
        System.out.println(10.0/3);

        //取模，取余。实际上也是做除法运算，只不过得到的值余数而已。
        System.out.println(10%2);
        System.out.println(10%3);

        //应用场景
        //用取模来判断，A是否可以被B整除
        //可以判断A是否位偶数  结果为0，为偶数

    }
}
