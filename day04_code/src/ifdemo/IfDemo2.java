package ifdemo;
/* if的注意点
1.大括号的开头建议写在第一行的末尾
2.在语句体中如果只有一句代码，大括号可以省略不写（建议不要省略）
3.如果要对布尔类型的变量进行判断，可直接将变量写在小括号内

 */
public class IfDemo2 {
    public static void main(String[] args) {
        int number = 1;
        if (number >= 10) {
            System.out.println("number大于等于10");

        }
    }
}